// 지도/대륙 퀴즈 — 국기/나라 → 대륙 맞추기
// 국기 퀴즈 연장판. GradeQuiz 호환 구조.

const CONTINENTS = ['아시아', '유럽', '아프리카', '아메리카', '오세아니아']

function q(flag, country, correctContinent, explanation) {
  // 정답 포함 4지선다 구성 (나머지는 다른 대륙)
  const others = CONTINENTS.filter(c => c !== correctContinent)
  // 셔플 대신 고정 순서 유지 (데이터 안정)
  const choices = [correctContinent, others[0], others[1], others[2]]
  // 정답 위치를 다양화하기 위해 아래처럼 수동 섞기
  return {
    q: `${flag} ${country}는 어느 대륙에 있을까요?`,
    choices,
    answer: 0,
    explanation,
  }
}

// 정답 위치 랜덤화 헬퍼 (데이터 정의는 생성 시 수동 배치)
function mix(question) {
  const { q: text, choices, answer, explanation } = question
  const correct = choices[answer]
  // 간단 랜덤 배치 (빌드 시점에 고정되진 않지만 첫 import 기준 고정됨)
  const pool = choices.filter((_, i) => i !== answer)
  const newChoices = [pool[0], pool[1], correct, pool[2]]
  return { q: text, choices: newChoices, answer: 2, explanation }
}

